public class arrayIndex {
    public static void main(String[] args) {
        int myArr = new int[5];
        myArr[0] = 98;
        myArr[1] = 83;
        myArr[2] = 91;
        myArr[3] = 65;
        myArr[4] = 56;
        System.out.println(myArr[0]);
        System.out.println(myArr[1]);
        System.out.println(myArr[2]);
        System.out.println(myArr[3]);
        System.out.println(myArr[4]);
    }
}
